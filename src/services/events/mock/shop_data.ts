import type { DialogEffects } from '@/services/client_player_service';

export interface ShopItem {
  /** ID товара в магазине */
  id: string;
  /** Название товара */
  title: string;
  /** Описание товара */
  description: string;
  /** Тип товара */
  type: 'consumable' | 'claim';
  /** Категория товара */
  category: string;
  /** Стоимость в монетах */
  cost: number;
  /** Эффекты товара */
  effects: DialogEffects;
  /** Условие для claim товаров */
  claimCondition?: {
    name: string;
    description: string;
    statusDefault: string;
  };
  /** Информация о продукте */
  productInfo?: {
    placeholderName: string;
    placeholderLink: string;
    mappedName?: string;
    mappedUrl?: string;
  };
  /** Ограничения доступности */
  availability: {
    startDay: number;
    endDay: number;
    dailyLimit: number;
    perUserLimit: number;
  };
  /** Изображение товара */
  imageUrl?: string;
  /** Приоритет отображения */
  priority: number;
  /** Оценка маппинга */
  mappingScore: number;
  /** Заметка о маппинге */
  mappingNote: string;
}

